 class solid_rhombus_pattern{
    
    public static void main(String[] args) {
     
    int n=1;    
for(int i=5;i>=1;i--){
    for(int j=1;j<=i-n;j++){
    System.out.print(" ");
    }

for(int j=1;j<=5;j++){
    System.out.print("*");
   
}
 System.out.println();


}
    }
}
